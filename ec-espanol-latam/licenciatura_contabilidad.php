    <h1>Licenciatura en Contabilidad <br />EN LÍNEA Y EN ESPAÑOL</h1><br />
    <h3 class="programasH3">Descripción del Programa</h3>
    La Licenciatura en Contabilidad de la Universidad de Keiser está enfocada 
    en la contabilidad, los negocios y las comunicaciones requeridas por el entorno 
    empresarial actual.<br />
    Nuestro programa proporciona al alumno las habilidades necesarias en diversas áreas de la contabilidad, tales como: impuestos, auditoría, gestión y costo, 
    financiero, gubernamental, y los sistemas de información de contabilidad, así como en materias de negocios en general.<br />
    La Licenciatura en Contabilidad también utiliza varios programas de gestión 
    de negocios y de contabilidad.<br />
    <br /><br />
    <h3 class="programasH3">OBJETIVOS DEL PROGRAMA</h3>
    La Licenciatura en Contabilidad tiene como objetivo principal preparar a los 
estudiantes para que puedan demostrar conocimiento de conceptos y normas 
de contabilidad y tributarias, indispensables en diversas áreas de especialidad dentro de la contabilidad, así como también instruirlos en el uso de herramientas 
y software específico.<br />
	<br />
    Obtén un título universitario con nuestra Licenciatura en Contabilidad en línea. Contacta a Keiser University en línea para más información.<br /><br />
    <p style="font-style: italic">Por favor, consulte las leyes locales de cada país, estado o región, para obtener detalles respecto a los requisitos para optar a la certificación de contador público (equivalente a CPA).</p><br />
    <span style="font-size:12px;">*Los candidatos deben poseer un diploma de secundaria (High School/GED) para ingresar al programa.</span>
