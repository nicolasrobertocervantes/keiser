    <h1>Maestría en Ciencias <span class="contentSpacer"><br /></span>en Psicología</h1>
    <br />
    <h3 class="programasH3">Descripción del Programa</h3>
    <p>La Maestría en Ciencias en Psicología de la Universidad de Keiser puede abrir las puertas a muchas oportunidades profesionales en la enseñanza, la consultoría o la evaluación y proporciona una base para el estudio de doctorado. El programa basado en la investigación se centra en el estudio científico de la psicología, la evaluación crítica de la investigación y la aplicación de los principios de investigación al campo de la psicología. Trabajará con nuestra facultad experimentada, que aporta su experiencia del mundo real al aula. El programa culmina en un proyecto Capstone que se basa en la base de la información aprendida en el trabajo del curso.</p>
    <p>En este programa, los estudiantes desarrollan una comprensión de los principios y teorías psicológicas generales, incluida la psicología evolutiva, la psicopatología, el desarrollo humano, la psicología de la salud y las pruebas y medidas. Los estudiantes aprenden a apreciar la diversidad en los individuos, demostrada a través de la aplicación de métodos multiculturales de investigación y la comprensión de los principios psicológicos. Los estudiantes también utilizarán métodos de investigación para interpretar y evaluar datos de investigación.</p>
    <p>Además de preparar el camino para estudios posteriores a nivel de doctorado, la M.S del programa en Psicología puede prepararlo para trabajar en una variedad de roles emocionantes, que incluyen:</p>
    <ul>
        <li>Instructor en un colegio comunitario o a nivel de pregrado</li>
        <li>Asistente de investigación o coordinador de investigación</li>
        <li>Evaluador de programas</li>
        <li>Consultor de investigación o educativo</li>
    </ul>
    <p>Este programa proporciona una base para el estudio científico de la psicología. No es un programa que conduce a la obtención de una licencia.</p>
    <p>Para obtener más información sobre nuestro programa de Maestría en Ciencias en Psicología en línea contactar a la División Latina de la Universidad de Keiser.</p>
