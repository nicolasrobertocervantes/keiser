    <h2>&iquest;Por qué estudiar en Keiser?</h2>
    <ul>
      <li><strong>En tu idioma.</strong> Clases completamente en español para tu conveniencia.</li>
      <li><strong>Accesible y flexible.</strong> En el mundo actual, el aprendizaje virtual lo prepara para el entorno global y al mismo tiempo le permite continuar con sus responsabilidades personales y profesionales. Usted podrá adaptar su horario de estudio a su conveniencia. También contamos con una tecnología avanzada en el aula virtual y un formato orientado a mejorar el aprendizaje del usuario.</li>
      <li><strong>Programas de una asignatura a la vez, por mes.</strong> Así podrá enfocarse en aprender, no tendrá que preocuparse por manejar un programa que requiere de un horario de varias asignaturas a la vez y que demanda múltiples tareas.</li>
      <li><strong>El estudiante está primero.</strong> Nuestros estudiantes son nuestra prioridad, y nuestro compromiso con el éxito estudiantil constituye el fundamento de nuestra misión como Universidad. Tenemos clases con un número reducido de estudiantes, dictadas por un cuerpo docente accesible.</li>
      <li><strong>Apoyo.</strong> Creemos que la atención individualizada crea un ambiente más apropiado para el aprendizaje. Usted recibirá orientación en función de sus objetivos individuales, sus intereses y las aspiraciones para su carrera.</li>
      <li><strong>Establecida.</strong> Por más de 40 años, Keiser University se ha dedicado a ayudar a cada estudiante a lograr sus metas personales y profesionales. Lo ha logrado a través de una educación práctica en carreras profesionales. Ofrecemos programas de asociados, licenciaturas, maestrías y doctorados, en línea y en español.</li>
    </ul>

    <h2>Acreditación</h2>
    <p>La Universidad Keiser está acreditada por la Comisión de Universidades de la Asociación Sureña de Universidades y Escuelas para otorgar títulos de asociado, licenciatura, maestría, especialista y doctorado. La Universidad Keiser también puede ofrecer certificados y diplomas en los niveles de pre-grado y post grado que fueran debidamente aprobados. Si tiene preguntas sobre la acreditación de la Universidad Keiser, estas pueden dirigirse por escrito a la Comisión de Universidades de la Asociación Sureña de Universidades y Escuelas a la siguiente dirección: 1866 Southern Lane, Decatur, Georgia 30033-4097, o llamando al (404) 679-4500, y/o utilizando la información disponible en Sitio web de SACSCOC <a href="https://www.sacscoc.org/" target="_blank">(www.sacscoc.org)</a>.</p>

