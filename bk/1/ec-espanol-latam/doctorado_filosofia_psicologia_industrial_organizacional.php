    <h3 class="programasH3">Descripción del Programa</h3>
    <p>El Doctorado en Filosofía en Línea en Psicología Industrial y Organizacional de la Universidad de Keiser es un doctorado en PhD basado en la investigación. Es un programa que prepara a sus egresados para roles de liderazgo con influencia directa en las metas y misiones de las empresas y organizaciones. A través de investigaciones, residencias y un riguroso proceso de disertación, los estudiantes de PhD saldrán del programa listos para enfrentar los desafíos que enfrentan los lugares de trabajo de hoy en día mientras navegan por una sociedad cambiante, en diversidad, ética y competencia global. Los estudiantes se enfocarán en las necesidades y los comportamientos de la gerencia y empleados para facilitar un cambio real y significativo en todos los niveles en las industrias públicas y privadas, así como en la investigación y la academia.</p>
    <p>Los estudiantes deben asistir a dos residencias en el campus.</p>
    <p>Como graduado del Doctorado en Psicología Industrial y Organizacional de la Universidad de Keiser podrás:</p>
    <ul>
        <li>Aplicar los principios y teorías de la psicología avanzada a temas organizacionales como la motivación, el comportamiento, el aprendizaje y la emoción</li>
        <li>Desarrollar programas y estándares destinados a promover y valorar la diversidad en el lugar de trabajo a nivel individual y organizacional</li>
        <li>Contribuir al cuerpo de conocimiento en psicología industrial y organizacional mediante el desarrollo de estrategias de investigación innovadoras y receptivas y la realización de investigaciones relevantes</li>
        <li>Aplicar una comprensión avanzada de la psicología organizacional industrial a los problemas prácticos que enfrentan tanto la gerencia como los empleados</li>
        <li>Servir como líder de pensamiento y parte clave en el desarrollo de técnicas y enfoques novedosos para problemas organizacionales críticos</li>
    </ul>
    <p>Comuníquese hoy con la División Latina de Keiser University para obtener más información sobre nuestro programa de Doctorado en Filosofía en Línea en Psicología Industrial y Organizacional.</p>
